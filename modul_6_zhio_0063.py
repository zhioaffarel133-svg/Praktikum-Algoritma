def urutkan_rating(film):
    for i in range(1, len(film)):
        kunci = film[i]
        j = i - 1
        # Geser film yang ratingnya lebih rendah ke belakang
        while j >= 0 and film[j]['rating'] < kunci['rating']:
            film[j + 1] = film[j]
            j -= 1
        film[j + 1] = kunci
    return film

