"""Tugas 4 — Fungsi find_path dengan DFS"""

def find_path(graph, start, end, visited=None, path=None):
    if visited is None:
        visited = set()
    if path is None:
        path = []

    visited.add(start)
    path = path + [start]

    if start == end:
        return path

    for neighbor in graph[start]:
        if neighbor not in visited:
            result = find_path(graph, neighbor, end, visited, path)
            if result is not None:
                return result
    return None

jalur = find_path(kota_graph, 'Jakarta', 'Yogyakarta')
print(jalur)

"""Tugas 5 — Fungsi is_connected dengan DFS"""

def is_connected(graph):
    if not graph:
        return True

    start = next(iter(graph))  # Ambil simpul pertama
    visited = set()

    def dfs_helper(node):
        visited.add(node)
        for neighbor in graph[node]:
            if neighbor not in visited:
                dfs_helper(neighbor)

    dfs_helper(start)
    return len(visited) == len(graph)

print(is_connected(kota_graph))
