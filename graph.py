from collections import deque

def bfs_return(graph, start):
    visited = set()
    queue = deque([start])
    order = []

    while queue:
        vertex = queue.popleft()
        if vertex not in visited:
            order.append(vertex)
            visited.add(vertex)
            queue.extend([
                n for n in graph[vertex]
                if n not in visited
            ])
    return order

